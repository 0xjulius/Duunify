export function computeDemoStats() {
  const statsData = DEMO_APPLICATIONS.reduce(
    (acc, app) => {
      const s = app.status?.toLowerCase().trim() || "";
      if (["suosikki", "tallennettu"].includes(s)) {
        acc.favorites++;
      } else if (["haastattelu", "interview"].includes(s)) {
        acc.interviews++;
      } else if (["tarjous", "offer"].includes(s)) {
        acc.offers++;
      } else if (["hylätty", "hylätyt", "rejected"].includes(s)) {
        acc.rejected++;
      } else if (["ghosted", "ei vastausta"].includes(s)) {
        acc.ghosted++;
      } else {
        acc.pending++;
      }
      return acc;
    },
    { favorites: 0, interviews: 0, offers: 0, rejected: 0, pending: 0, ghosted: 0 },
  );

  const totalActive =
    statsData.pending +
    statsData.interviews +
    statsData.offers +
    statsData.rejected +
    statsData.ghosted;

  return {
    total: totalActive,
    pending: statsData.pending,
    offers: statsData.offers,
    interviews: statsData.interviews,
    rejected: statsData.rejected,
    favorites: statsData.favorites,
    ghosted: statsData.ghosted, // <-- LISÄTTY TÄMÄ RIVI
    consistency: 71,
  };
}

export function computeDemoLocationStats() {
  const counts: Record<string, number> = {};
  DEMO_APPLICATIONS.forEach((a) => {
    if (a.location) counts[a.location] = (counts[a.location] || 0) + 1;
  });
  return Object.entries(counts)
    .map(([name, count]) => ({ name, count }))
    .sort((a, b) => b.count - a.count);
}

export function daysFromNow(offset: number, hour = 9): string {
  const d = new Date();
  d.setDate(d.getDate() + offset);
  d.setHours(hour, 0, 0, 0);
  return d.toISOString();
}

export function dateOnlyFromNow(offset: number): string {
  const d = new Date();
  d.setDate(d.getDate() + offset);
  return d.toISOString().split("T")[0];
}

